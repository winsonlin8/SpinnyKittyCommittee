print(f"Generated music_data.h with {len(scaled_data)} points.")

"""Making CSV file"""

OUTPUT_CSV = "motor.csv"
TARGET_INTERVAL_MS = 20  # 20ms frame

# 1. Computing RMS energy
# hop_length = sr * interval_sec
hop_length = int(sr * (TARGET_INTERVAL_MS / 1000.0))
rms = librosa.feature.rms(y=y, hop_length=hop_length)[0]

# 2. Normalizing RMS
rms_norm = rms / (rms.max() if rms.max() > 0 else 1)
motor_values = (rms_norm ** 2) * 255  # scale to 0-255

# 3. Make data frame
time_ms = np.arange(len(motor_values)) * TARGET_INTERVAL_MS
df = pd.DataFrame({
    'time_ms': time_ms.astype(int),
    'motor': motor_values.astype(int)
})

# 4. Save CSV
df.to_csv(OUTPUT_CSV, index=False)
print(f"Saved {len(df)} frames to {OUTPUT_CSV} (Duration: {duration:.2f}s)")