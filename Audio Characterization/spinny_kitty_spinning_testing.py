time_s = time_ms / 1000.0

# Interpolate PWM onto fixed time base
pwm_fixed = np.interp(time_s, rms_times, pwm_values)

# Optional smoothing to remove jitter
SMOOTH_FRAMES = 3
kernel = np.ones(SMOOTH_FRAMES) / SMOOTH_FRAMES
pwm_fixed = np.convolve(pwm_fixed, kernel, mode='same')

# Final integer PWM
pwm_fixed = np.clip(np.round(pwm_fixed), PWM_MIN, PWM_MAX).astype(np.uint8)

print("Compressed frame count:", len(pwm_fixed))
print("Compressed PWM range:", int(pwm_fixed.min()), "–", int(pwm_fixed.max()))
print("Approx storage bytes:", len(pwm_fixed))

