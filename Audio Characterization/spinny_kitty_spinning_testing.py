    f.write("#ifndef MUSIC_DATA_H\n")
    f.write("#define MUSIC_DATA_H\n\n")
    f.write("#include <stdint.h>\n\n")
    f.write(f"#define DATA_COUNT ({len(pwm_fixed)}U)\n")
    f.write(f"#define UPDATE_INTERVAL_MS ({FRAME_MS}U)\n\n")
    f.write("const uint8_t pwm_data[DATA_COUNT] = {\n")

    for i, val in enumerate(pwm_fixed):
        if i % 16 == 0:
            f.write("    ")
        f.write(f"{int(val)}, ")
        if i % 16 == 15:
            f.write("\n")

    if len(pwm_fixed) % 16 != 0:
        f.write("\n")

    f.write("};\n\n")
    f.write("#endif\n")

