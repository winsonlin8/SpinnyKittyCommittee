HOP_LENGTH  = 512       # analysis resolution
ONSET_DELTA = 0.18      # higher = fewer color changes

# Comet appearance
BRIGHT_MIN  = 40
BRIGHT_MAX  = 255
TAIL_MIN    = 2
TAIL_MAX    = 3
STEP_MIN    = 1
STEP_MAX    = 3

# Color palette — color_idx (0-7) stored in sparkle column
BEAT_PALETTE = [
    (255,   0,   0),
    (  0, 255,   0),
    (  0,   0, 255),
    (255, 165,   0),
    (128,   0, 255),
    (  0, 255, 200),
    (255, 255,   0),
    (255,  20, 147),
]

# ── Uses y, sr already loaded above ──────────────────────────────────────────

def normalize(x):
    return (x - x.min()) / (x.max() - x.min() + 1e-9)

# Energy
rms      = librosa.feature.rms(y=y, hop_length=HOP_LENGTH)[0]
rms_norm = normalize(rms)

# Onset detection
onset_frames = librosa.onset.onset_detect(
    y=y, sr=sr, hop_length=HOP_LENGTH,
    delta=ONSET_DELTA, units='frames'
)
onset_set = set(onset_frames.tolist())
print(f"Onsets detected: {len(onset_frames)}")

# ── Build full analysis-rate frame list ──────────────────────────────────────
analysis_dt_s   = HOP_LENGTH / sr
n_analysis      = len(rms_norm)
palette_idx     = 0
pos             = 0
analysis_frames = []   # (pos, bright, tail, color_idx)

for i in range(n_analysis):
    energy = float(rms_norm[i])

    if i in onset_set:
        palette_idx = (palette_idx + 1) % len(BEAT_PALETTE)

    step   = int(round(STEP_MIN + energy * (STEP_MAX - STEP_MIN)))
    bright = int(np.clip(BRIGHT_MIN + energy * (BRIGHT_MAX - BRIGHT_MIN), 0, 255))
    tail   = int(round(TAIL_MIN + energy * (TAIL_MAX - TAIL_MIN)))
    pos    = (pos + step) % NUM_LEDS

    analysis_frames.append((pos, bright, tail, palette_idx))

# ── Resample to exact 20 ms grid ─────────────────────────────────────────────
duration_s   = len(y) / sr
frame_dt_s   = FRAME_DT_MS / 1000.0
n_out_frames = int(duration_s / frame_dt_s)   # total 20 ms frames in song

rows = []
for f in range(n_out_frames):
    t_s          = f * frame_dt_s
    ana_idx      = int(round(t_s / analysis_dt_s))
    ana_idx      = min(ana_idx, n_analysis - 1)

    pos_v, bright_v, tail_v, cidx = analysis_frames[ana_idx]

    # Clamp to CSV spec ranges
    pos_v    = int(np.clip(pos_v,    0,  34))
    bright_v = int(np.clip(bright_v, 0, 255))
    tail_v   = int(np.clip(tail_v,   1,  35))
    sparkle  = int(np.clip(cidx,     0, 255))  # color_idx stored in sparkle

    rows.append((f * FRAME_DT_MS, pos_v, bright_v, tail_v, sparkle))

# ── Write CSV ─────────────────────────────────────────────────────────────────
with open(OUT_CSV, "w", newline="") as fh:
    writer = csv.writer(fh)
    writer.writerow(["time_ms", "pos", "bright", "tail_len", "sparkle"])
    writer.writerows(rows)

