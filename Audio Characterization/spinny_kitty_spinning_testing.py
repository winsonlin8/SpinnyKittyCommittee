# ---------------------------------------------------------
# Tunable parameters
# ---------------------------------------------------------
PWM_MIN = 40       # minimum motor speed so it doesn't stall
PWM_MAX = 255      # maximum motor speed
BEAT_BOOST = 80    # stronger boost on beat
ONSET_BOOST = 40   # smaller boost on onset
DECAY = 0.85       # boost decay
FRAME_MS = 20      # compressed output time base for MSPM0

# ---------------------------------------------------------
# Build source PWM timeline at RMS-frame resolution
# ---------------------------------------------------------
pwm_values = np.zeros(len(rms_times), dtype=float)

# baseline from RMS energy
for i in range(len(rms_times)):
    base = PWM_MIN + rms_norm[i] * (PWM_MAX - PWM_MIN)
    pwm_values[i] = base

# beat boosts
for beat_t in beat_times:
    idx = np.argmin(np.abs(rms_times - beat_t))
    boost = BEAT_BOOST
    j = idx
    while j < len(pwm_values) and boost > 1:
        pwm_values[j] = min(PWM_MAX, pwm_values[j] + boost)
        boost *= DECAY
        j += 1

# onset boosts
for onset_t in onset_times:
    idx = np.argmin(np.abs(rms_times - onset_t))
    boost = ONSET_BOOST
    j = idx
    while j < len(pwm_values) and boost > 1:
        pwm_values[j] = min(PWM_MAX, pwm_values[j] + boost)
        boost *= DECAY
        j += 1

# clip valid range
pwm_values = np.clip(pwm_values, PWM_MIN, PWM_MAX)

print(f"Source PWM range: {pwm_values.min():.0f} – {pwm_values.max():.0f}")

